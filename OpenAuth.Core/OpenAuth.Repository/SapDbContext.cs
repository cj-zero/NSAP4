        protected override void OnModelCreating(ModelBuilder modelBuilder)
        {
            base.OnModelCreating(modelBuilder);
            #region 业务伙伴
            modelBuilder.Entity<OCRD>().HasKey(o => o.CardCode);
            modelBuilder.Entity<OSLP>().HasKey(o => o.SlpCode);
            modelBuilder.Entity<OCRG>().HasKey(o => o.GroupCode);
            modelBuilder.Entity<OIDC>().HasKey(o => o.Code);
            modelBuilder.Entity<OHEM>().HasKey(o => o.empID);
            modelBuilder.Entity<OCRY>().HasKey(o => o.Code);
            modelBuilder.Entity<OCST>().HasKey(o => new { o.Code, o.Country });
            modelBuilder.Entity<OCPR>().HasKey(o => o.CntctCode);
            modelBuilder.Entity<CRD1>().HasKey(o => new { o.CardCode,o.Address});
            #endregion
            #region 序列号
            modelBuilder.Entity<OINS>().HasKey(o => o.insID);
            modelBuilder.Entity<CTR1>().HasKey(o => new { o.ContractID, o.Line });
            modelBuilder.Entity<OSRIModel>().HasKey(o => o.SuppSerial);
            #endregion
            #region 物料编码
            modelBuilder.Entity<OITM>().HasKey(o => o.ItemCode);
            modelBuilder.Entity<OITW>().HasKey(o => new { o.ItemCode, o.WhsCode });
            modelBuilder.Entity<ITT1>().HasKey(o => new { o.Father, o.ChildNum });
            modelBuilder.Entity<WOR1>().HasKey(o => new { o.DocEntry, o.LineNum });
            modelBuilder.Entity<SysEquipmentColumn>().HasKey(o => o.ItemCode);
            #endregion
            #region 销售订单
            modelBuilder.Entity<ORDR>().HasKey(o => o.DocEntry);
            modelBuilder.Entity<RDR1>().HasKey(o => o.DocEntry);
            #endregion

            modelBuilder.Entity<OITL>().HasKey(o => o.LogEntry);
            modelBuilder.Entity<ITL1>().HasKey(o => new { o.LogEntry, o.ItemCode, o.SysNumber, });
            modelBuilder.Entity<OSRN>().HasKey(o => o.AbsEntry);
            #region 销售交货
            modelBuilder.Entity<ODLN>().HasKey(o => o.DocEntry);
            modelBuilder.Entity<DLN1>().HasKey(o => new { o.DocEntry,o.LineNum});
            #endregion
            #region 应收贷项凭证
            modelBuilder.Entity<ORIN>().HasKey(o => o.DocEntry);
            modelBuilder.Entity<RIN1>().HasKey(o => new { o.DocEntry,o.LineNum });
            modelBuilder.Entity<OINV>().HasKey(o => o.DocEntry);
            modelBuilder.Entity<INV1>().HasKey(o => new { o.DocEntry, o.LineNum });
            #endregion

            //为含有decimal类型属性的实体设置decimal的精度(不设置efcore会提示warning:This will cause values to be silently truncated if they do not fit in the default precision and scale.)
            foreach (var item in modelBuilder.Model.GetEntityTypes().Where(t => t.ClrType.GetProperties().Any(p => p.PropertyType == typeof(decimal) || p.PropertyType == typeof(decimal?))))
            {
                var type = item.ClrType;
                var props = type.GetProperties().Where(p => p.PropertyType == typeof(decimal) || p.PropertyType == typeof(decimal?));
                foreach (var p in props)
                {
                    modelBuilder.Entity(type).Property(p.Name).HasColumnType("decimal(19,6)"); //这个精度范围参考Sap的sql server里面的numeric类型精度
                }
            }
        }
